                                        <p>Have you ever gone through an old photo album and just cringe at the haircut you rocked back in the day?</p>
                                        <p>Yeah…</p>
                                        <p>This post is kind of like that, but we’ll be going way, way back in history. Join me on a hair journey and see what lengths people from the past will go to be free as their hair.</p>
                                        <h3><strong>Mullets</strong></h3>
                                        <p>One haircut proves you can have both: short and long, business and party, love and hate.</p>
                                        <p>Ancient civilizations have sported the look in favor of its dual-functionality. Admittedly, it keeps strands away from the eyes while keeping the neck warm.</p>
                                        <p>Pop culture icons like David Bowie have elevated it to the epitome of anti-establishment cool in the ‘70s and ‘80s.</p>
                                        <p>So what has the mullet done to deserve such infamy? Reddit user <a class="link-white" href="https://www.reddit.com/user/smokebreak" target="_blank" rel="nofollow">smokebreak</a> presents an interesting <a class="link-white" href="https://www.reddit.com/r/AskReddit/comments/tizif/why_is_the_mullet_haircut_so_hated/c4n0lls/" target="_blank" rel="nofollow">theory</a> about symbolism and connotations:</p>
                                        <p><em class="text-red">“Mullets are like the Confederate flag. Beautiful, perhaps, but they get associated with all kinds of unfortunate things. Mullets are associated with the 80s, and with people who marry their sisters and proudly live off welfare in their newly renovated double wide.”</em></p>
                                    
                                        <h3><strong>Powdered Wigs (Perukes)</strong></h3>
                                        <p>By the 17th century, Europe is ravaged by syphilis-induced sores and bald spots, not to mention lice infestations. Wigs became a way to save face in a time where hair is seen as an extension of one’s health and social status.</p>
                                        <p>The trend took a turn for the unapologetically decadent when the rising upper class, eager to flaunt their new wealth, began emulating the designer wigs of King Luis XIV. This went hand in hand with the rise of the Rococo aesthetic.</p>
                                        <p>According to <a class="link-white" href="https://thehistoryofthehairsworld.com/hair_18th_century.html" target="_blank" rel="nofollow">thehistoryofthehairsworld</a> blog, <em class="text-red">“The eighteenth century was an age of elegance. Never in European history do we see men and women so elaborately artificial, so far removed from natural appearance.”</em></p>
                                        <p>Leonard Autie, the mastermind behind Marie-Antoinette’s infamous pouf, had concocted hairstyles so tall and indulgent that it made a woman’s head appear as if it were at the middle of her body.</p>
                                        <p>This façade of sophistication comes with a whole set of inconveniences though. Not only were these wigs expensive, they demand maintenance. Women who donned elaborate wigs also had to deal with migraines, fire prevention, theft and rats.</p>

                                        <h3><strong>‘80s Big Hair</strong></h3>
                                        <p>Beauty icons have come and gone but none as startlingly fascinating as Elizabeth I. The Virgin Queen’s pale complexion, high forehead, and brilliant Tudor red hair became the standard of beauty during her reign.</p>
                                        <p>“Elizabeth’s contemporaries believed that beauty amplified female power, and so they regarded the queen’s splendor as confirmation of her claim to the throne,” historian Dr. Anna Whitelock explains.</p>
                                        <p>Women of this era who wished to reach this standard shaved off their lashes and plucked their brows into thin arches in order to achieve her high forehead. The most bizarre practices though involved bleaching hair with urine and plucking hairlines unnaturally far up the head in order to mimic aristocracy.</p>

                                        <h3><strong>Elizabethan Hairlines</strong></h3>
                                        <p>The ‘80s is a repeat offender when it comes to bad hair trends. The MTV era ushered in an unprecedented surge of pop stars and rock bands who emphasized outrageous visuals to push their music. That meant big hair could never be big enough.</p>
                                        <p>In contrast to the massive beehives of the ‘60s though, locks were teased and poofed within an inch of their lives. The result made everyone look wild, wacky and like they had poodles on their shoulders. Looking back, it’s hilarious in an embarrassing way.</p>
                                        <br>
                                        <p>History is a treasure-trove of the strange and the fascinating, especially for hair. It’s one facet of our appearance that can communicate who we were, what we found beautiful and what we value as a society back in the day. But be warned, future generations may look at our man buns and mermaid hair with a snarky guffaw.</p>
