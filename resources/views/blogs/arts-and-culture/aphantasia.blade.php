                                        <p>Did you know that some people can <em>literally</em> visualize images in their minds? If that isn’t surprising, you should know that some people <em>can’t</em>.</p>
                                        <p>I know, it may sound crazy but I can't summon mental images in my mind too.</p>
                                        <p>This condition is called <em>aphantasia</em> which describes one’s inability to conjure mental images. The tricky part with this condition is that professionals can't really diagnose a person if they have <em>aphantasia</em> because it’s not really considered as a disability.</p>
                                        <p>Early accounts of this phenomenon go way back as early as 1880 conducted by Sir Francis Galton and have been unstudied ever since. It’s a relatively new condition being brought up and studied by modern scientists, so we don’t really know much of this condition—or just not yet.</p>
                                        <p>When I first tried guided meditation, I have to admit that I’ve always had a hard time <em>visualizing</em> the scenes the narrator tells me.</p>
                                        <p>“Imagine you are in a beach…”</p>
                                        <p>“Imagine you are walking in the middle of a forest…”</p>
                                        <p>“Imagine you are on top of a mountain…”</p>
                                        
