                                        <p>It’s just black. Nothing.</p>
                                        <p>I’ve always thought I wasn’t doing it right, but I kept doing it anyway. As I learn that some people have the ability to vividly imagine things in their minds, I felt so envious. I wanted to experience what it’s like to meditate and see what my “higher self” has to offer.</p>
                                        <p>But I just can't. I was denying everything at first.</p>
                                        <p>On one hand, if I do have <em>aphantasia</em>, I’ll have this overwhelming and constant burden of wondering how it actually feels to visualize things inside my head.</p>
                                        <p>And on the other hand, if this is just a case of poor imagination, I’ll feel unimaginative and undeserving of my career--- especially that I’m writing and making films for a living.</p>
                                        
                                        <p class="text-center">
