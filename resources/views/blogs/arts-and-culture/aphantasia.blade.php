                                        <p>This is a glass-shattering moment for me. Looking back, my childhood felt like a lie. I wondered how daydreaming went for some people now that I realize that maybe, just maybe, my “daydreaming techniques” were wrong.</p>
                                        <p>As the glass continued to shatter, the following days for me have been a constant ride of bewilderment.</p>
                                        <p>I felt left out.</p>
                                        <p>Until I’ve come to my senses and realized something.</p>
                                        <p>“Hey, I made it this far.”</p>
                                        <p>And it’s true. I’ve made it this far. So what if I have <em>aphantasia</em>? So what if I have a weak imagination? If I really think about it, I’m not really at a disadvantage.</p>
                                        <p>I’ve lived my life just fine not knowing what <em>aphantasia</em> was.</p>
                                        <p>The label made me a victim, and I associated my “weaknesses” to the label.  Just because someone is capable or better at visualizing, doesn’t mean I'm terrible at doing my job or losing in life. It’s really how we make of it.</p>
                                        <p>I know I'm an over-thinker to the point where my internal voice doesn’t literally shut up. It’s like there’s a voice in my head, narrating what I know instead of visualizing them. It has helped me a lot in processing information.</p>
                                        <p>When someone tells me to close my eyes and imagine an apple, I don’t necessarily see an apple at the back of my mind. No mental image, whatsoever.</p>
                                        <p class="text-center">
                                            <img src="{{{ url('storage/blogs/arts-and-culture/apple-food-fruit.jpg') }}}" alt="Apple-Food-Fruit" style="width:100%">
                                        </p>
                                        <br>
                                        <p>Instead, an internal voice processes what I know of an apple. An apple commonly comes with a red or green color. It’s somewhat round similar to the size of a human fist.</p>
                                        <p>It seems like my brain associates the idea of an object <em>conceptually</em> rather than seeing it as an object visually.</p>
                                        <p>As <a class="link-white" href="https://themighty.com/2018/06/aphantasia-not-able-to-picture-imagine-things/" target="_blank" rel="nofollow">B.L. Acker</a> states in her piece regarding her experiences living with <em>aphantasia</em>, the mind works as if it races to a series of database that pulls out words to describe what was asked to visualize.</p>
                                        <p>Mozilla founder, <a class="link-white" href="https://themighty.com/2018/06/aphantasia-not-able-to-picture-imagine-things/" target="_blank" rel="nofollow">Blake Ross</a>, also wrote an interesting piece with his experiences living with <em>aphantasia</em>. You should check it out if you haven’t!</p>
                                        <p>For those who have <em>aphantasia</em>, or if you think that you have it, please don’t let it define you. It’s not something we need to overcome. It’s just what it is.</p>
                                        <p>As cheesy as it sounds, we are unique in our own little ways.</p>
